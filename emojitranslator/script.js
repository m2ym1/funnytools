textArea.addEventListener("input", function (event) {
  var inputText = event.target.value;
  var translatedText = translateToEmoji(inputText);
  document.getElementById("text2").innerHTML = translatedText;
});

function translateToEmoji(text) {
  var words = text.toLowerCase().split(" ");
  var translated = "";

  for (var i = 0; i < words.length; i++) {
    var word = words[i];
    if (emojiMappings[word]) {
      translated += emojiMappings[word] + " ";
    } else {
      translated += word + " ";
    }
  }

  return translated.trim();
}
